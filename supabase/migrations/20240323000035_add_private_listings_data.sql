insert into cars (
  brand_id,
  make,
  model,
  year,
  price,
  image,
  savings,
  condition,
  mileage,
  fuel_type,
  transmission,
  body_type,
  exterior_color,
  interior_color,
  number_of_owners,
  is_sold
)
select
  brand_id,
  make,
  model,
  year,
  price,
  image,
  floor(price * 0.1), -- 10% savings
  condition,
  mileage,
  fuel_type,
  transmission,
  body_type,
  exterior_color,
  interior_color,
  number_of_owners,
  false
from private_listings
where status = 'approved';